export function monthToString(it) {
  if (it == 0) {
    return "Jan";
  }
  if (it == 1) {
    return "Feb";
  }
  if (it == 2) {
    return "Mar";
  }
  if (it == 3) {
    return "Apr";
  }
  if (it == 4) {
    return "May";
  }
  if (it == 5) {
    return "Jun";
  }
  if (it == 6) {
    return "Jul";
  }
  if (it == 7) {
    return "Aug";
  }
  if (it == 8) {
    return "Sep";
  }
  if (it == 9) {
    return "Oct";
  }
  if (it == 10) {
    return "Nov";
  }
  if (it == 11) {
    return "Dec";
  }
}
export function dayToString(it) {
  if (it == 0) {
    return "Sunday";
  }
  if (it == 1) {
    return "Monday";
  }
  if (it == 2) {
    return "Tuesday";
  }
  if (it == 3) {
    return "Wednesday";
  }
  if (it == 4) {
    return "Thuesday";
  }
  if (it == 5) {
    return "Friday";
  }
}
